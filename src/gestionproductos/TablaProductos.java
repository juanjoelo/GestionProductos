    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
       String nombre = NombreDelProducto.getText();
       float precio =Float.parseFloat( PrecioDelProducto.getText());
       String categoria = (String)this.Categoria.getSelectedItem();
       DefaultTableModel dtm = (DefaultTableModel)this.Tabla.getModel();
      
       Object [] row={categoria, nombre, precio };
       dtm.addRow(row);
       Tabla.setModel(dtm);
        NombreDelProducto.setText("");
        PrecioDelProducto.setText("");
        NombreDelProducto.requestFocus();
    }//GEN-LAST:event_jButton1ActionPerformed

